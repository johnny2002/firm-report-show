 package com.ibm.gbsc.reportadapter.cognos;
 
 public class CognosText
 {
   private String reportId;
   private String fileTextId;
   private String reportingDate;
   private String owner;
 
   public String getReportId()
   {
     return this.reportId;
   }
 
   public void setReportId(String reportId)
   {
     this.reportId = reportId;
   }
 
   public String getFileTextId()
   {
     return this.fileTextId;
   }
 
   public void setFileTextId(String fileTextId)
   {
     this.fileTextId = fileTextId;
   }
 
   public String getReportingDate()
   {
     return this.reportingDate;
   }
 
   public void setReportingDate(String reportingDate)
   {
     this.reportingDate = reportingDate;
   }
 
   public String getOwner()
   {
     return this.owner;
   }
 
   public void setOwner(String owner)
   {
     this.owner = owner;
   }
 }
